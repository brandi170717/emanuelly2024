// Usando incremento 
for ($i = 0; $i <= 10; $i ++) {
    echo $i . " "; // Saída: 00, 01, 02, 03, 04, 05, 06, 07, 08, 09, 10.
}

echo'<br>';
// usando decremento 
for ($i = 10; $i >= 0; $i--) {
    echo $i . " "; //Saída: 10, 09, 08, 07, 06, 05, 04, 03, 02, 01, 00.
}
?>